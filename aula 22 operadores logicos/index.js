/*
Operadoes Lógicos
&& -> and -> e (toda expressão precisa ser verdadeira)
|| -> or -> ou (a expressão pode ser verdadeira ou falsa sendo atendido a uma condição)
!  -> not -> não (a expressão é negada, invertida)

*/
const expressaoAnd = true && true && true;
console.log(expressaoAnd);

const expressaoOr = true || false;
console.log(expressaoOr);

const expressaoNot = !true;
console.log(expressaoNot);