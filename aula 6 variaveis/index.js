/* Criando variáveis, existem as duas opções abaixo */
// o (var) tbm cria variavel, porém não se utiliza tanto.
// não podemos criar variáveis com palavras reservadas do sistema
// As variáveis precisam ter nomes com significado para o sistema
// Não pode começar a variável com um número, não pode conter espaços ou traços
// As variáveis são case-sensitive, as maiúsculas são diferentes de minúsculas 
// Não podemos redeclarar variáveis com (let)

let nome = 'Luciano';


console.log(nome, 'nasceu em 1980');
console.log('Em 2000', nome, 'tinha 20 anos');
console.log('O tempo não para', nome);
console.log(nome, 'a Piscina está cheia de água');
console.log(nome, 'Ano cheio de mudanças');

let nome1; // só declarou a variável
nome1 = 'Qualquer nome'; // Inicializando a variável
console.log(nome1);